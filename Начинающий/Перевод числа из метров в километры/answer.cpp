#include <iostream>

using namespace std;

int main()
{
   double meters;

   cout << "Введите число в метрах: ";
   cin >> meters;

   double kilometers = meters / 1000;

   cout << meters << " метров = " << kilometers << " километров" << endl;

   return 0;
}

