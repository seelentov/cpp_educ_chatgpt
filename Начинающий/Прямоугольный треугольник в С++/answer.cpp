#include <iostream>

using namespace std;

int main()
{
   int n = 5; // Высота треугольника

   for (int i = 1; i <= n; i++)
   {
      // Печатаем i плюсов
      for (int j = 1; j <= i; j++)
      {
         cout << "+";
      }
      // Переход на новую строку
      cout << endl;
   }
   return 0;
}

// Объяснение:

// 1. Ввод:  Программа принимает высоту треугольника (n) от пользователя.
// 2. Внешний цикл: Цикл for с переменной i проходит по каждой строке треугольника.
// 3. Внутренний цикл: Цикл for с переменной j печатает i плюсов в каждой строке.
// 4. Переход на новую строку: После каждой строки печатается endl, чтобы перейти на следующую строку.

// Пример вывода:+
// ++
// +++
// ++++
// +++++