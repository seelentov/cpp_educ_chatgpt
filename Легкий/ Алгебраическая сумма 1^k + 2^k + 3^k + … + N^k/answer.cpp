#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    int N, k;

    cout << "Введите N: ";
    cin >> N;

    cout << "Введите степень k: ";
    cin >> k;

    int sum = 0;

    for (int i = 1; i <= N; i++)
    {
        sum += i * std::pow(k, i); //  i * k^i
    }

    cout << "Сумма: " << sum << endl;

    return 0;
}

// Объяснение:

// 1. Заголовки:
//    - #include <iostream>:  включает библиотеку ввода-вывода.
//    - using namespace std;:  упрощает код, используя пространство имен std.

// 2. Переменные:
//    - N: хранит значение, которое вводит пользователь для N.
//    - k: хранит значение, которое вводит пользователь для степени k.
//    - sum:  хранит сумму членов ряда.

// 3. Ввод данных:
//    -  cout << "Введите N: ";  выводит текст, просящий пользователя ввести значение N.
//    -  cin >> N;  считывает значение N от пользователя.
//    - То же самое повторяется для ввода степени k.

// 4. Цикл for:
//    -  for (int i = 1; i <= N; i++):  цикл повторяется N раз, при этом переменная i принимает значения от 1 до N включительно.
//    -  sum += i * pow(k, i);:  в каждой итерации цикла к сумме sum прибавляется значение i * k^i. Функция pow(k, i) вычисляет k в степени i.

// 5. Вывод результата:
//    -  cout << "Сумма: " << sum << endl;  выводит полученную сумму sum.