#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    int n;
    double Y = 0;

    cout << "Введите количество X: ";
    cin >> n;

    for (int i = 0; i < n; i++)
    {
        double Z, B, A;

        cout << "Введите Z для X" << i + 1 << ": ";
        cin >> Z;
        cout << "Введите B для X" << i + 1 << ": ";
        cin >> B;
        cout << "Введите A для X" << i + 1 << ": ";
        cin >> A;

        double X = pow(Z, 3) - B + pow(A, 2);
        Y += X;
    }

    cout << "Y = " << Y << endl;

    return 0;
}

// Объяснение:

// 1. Ввод данных:
//    - Программа запрашивает количество X (n) от пользователя.
//    - В цикле for (n раз) она запрашивает значения Z, B, A для каждого X.

// 2. Расчет X:
//    - Внутри цикла рассчитывается X, используя формулу X = Z^3 - B + A^2.
//    - Для возведения в степень используется функция pow(base, exponent).

// 3. Расчет Y:
//    - Значение X суммируется с Y в каждой итерации цикла.

// 4. Вывод результата:
//    - После завершения цикла выводится значение Y.